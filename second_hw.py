
age =int(input('enter your age :'))

if 1 <= age <6 :
    print('you are infant')

elif 6 <= age <18 :
    print('you are teenager')

elif 18 <= age <45 :
    print('you are young')

elif 45 <= age <60 :
    print('you are middle_age')

elif 60 <= age :
    print('you are  old')

else:
    print('Error')
